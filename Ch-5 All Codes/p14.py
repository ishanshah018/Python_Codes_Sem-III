#  Some SET Operations to learn:

s1 = {1,2,3,4,5}
s2 = {4,5,6,7,8}
# Union(|)
print(s1 | s2,"Union(|)")
# Intersection(&)
print(s1 & s2,"Intersection(&)")
# Difference(-)
print(s1 - s2,"s1-s2 Difference(-)")
print(s2 - s1,"s2-s1 Difference(-)")
# Symmetric Difference(^)
print(s1 ^ s2,"Symmetric Difference(^)")
